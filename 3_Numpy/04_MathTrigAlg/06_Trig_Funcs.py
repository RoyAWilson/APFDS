import numpy as np

a = np.array([1, 2, 3])

print(f'The sin of the array\n{a} is\n{np.sin(a)}')
print(f'The cos of the array\n{a} is\n{np.cos(a)}')
print(f'The tan of the array\n{a} is\n{np.tan(a)}')
